	private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
	if (startButton.getText().equals("Avvia server") || startButton.getText().equals("Riavvia server")) {
		switch (chat.createDBConnection(ipField.getText(), portField.getText())) {
			case -2:
				JOptionPane.showMessageDialog(this, "Driver JDBC non trovato!", "Errore!", JOptionPane.ERROR_MESSAGE);
				break;
			case 0:				 //host and port errors
				JOptionPane.showMessageDialog(this, "Attenzione!\nIndirizzo Host o porta sbagliata (o non raggiungibile)!", "ERRORE!", JOptionPane.ERROR_MESSAGE, null);
				break;
			case 1045:				//username and password errrors
				JOptionPane.showMessageDialog(this, "Attenzione!\nNome utente o password sbagliata!", "ERRORE!", JOptionPane.ERROR_MESSAGE, null);
				break;
			case 1049:				//database not found errors
				JOptionPane.showMessageDialog(this, "Attenzione!\nNome database errato!", "ERRORE!", JOptionPane.ERROR_MESSAGE, null);
				break;
			default:				//no errors found
				startButton.setText("Termina server");
				statusLabel.setForeground(Color.green);
				openUsersManagement.setEnabled(true);
				this.updateUsersTable();
		}
	} else {
		startButton.setText("Riavvia server");
		statusLabel.setForeground(Color.red);
		openUsersManagement.setEnabled(false);
		chat.disconnectFromDB();
	}
	}//GEN-LAST:event_startButtonActionPerformed

