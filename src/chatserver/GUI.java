	private void addUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserButtonActionPerformed
	if (!usernameTextField.getText().isEmpty() || !passwordTextField.getText().isEmpty()) {
		if(passwordTextField.getText().equals(retypePasswordTextField.getText())) {
			chat.insertUser(usernameTextField.getText(), passwordTextField.getText().toString());
			updateUsersTable();
			usernameTextField.setText("");
			passwordTextField.setText("");
			retypePasswordTextField.setText("");
		} else
			JOptionPane.showMessageDialog(this, "Attenzione!\nLe password non corrispondono!", "ERRORE!", JOptionPane.ERROR_MESSAGE, null);
	}
	else
		JOptionPane.showMessageDialog(this, "Attenzione!\nCampi username e/o password non compilati!", "ERRORE!", JOptionPane.ERROR_MESSAGE, null);
	}//GEN-LAST:event_addUserButtonActionPerformed

